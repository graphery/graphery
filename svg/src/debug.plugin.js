const isValidElement          = (parent, child) => ELEMENTS[ parent ] && ELEMENTS[ parent ].includes (child);
const isValidMethod           = (gySVGObject, tag, f) => {
  return (
    f.indexOf ('style.') === 0 ?
      isValidStyleMethod (tag, f.substring (6)) :
      f.indexOf ('classList.') === 0 ?
        isValidClassListMethod (tag, f.substring (10)) :
        gySVGObject.prototype[f] || !!METHODS[ f ]
  );
};
const isValidMethodParameters = (tag, f) => true;
const isValidStyleMethod      = (tag, f) => !!STYLE.includes (f);
const isValidClassListMethod  = (tag, f) => !!CLASSLIST.includes (f);
const isValidPathD            = (tag, f) => !!D.includes(f);

export default function debugPlugin (gySVG, gySVGObject) {

  // Update gySVG
  Object.assign (
    gySVG,
    {
      _debug: 0,
      debugLevel (level) {
        gySVG._debug = level;
      },
      get DEBUG_NONE () {
        return 0;
      },
      get DEBUG_ERROR () {
        return 1;
      },
      get DEBUG_WARNING () {
        return 2;
      },
      get DEBUG_ALL () {
        return 3;
      },
      _call (wrapped, prop, args) {
        if (gySVG._debug >= gySVG.DEBUG_ALL) {
          console.info (`${ NAME } call gySVG( ${ toString (args) } )`);
        }
      },
    }
  );
  // Update gySVGObject
  Object.assign (
    gySVGObject.prototype,
    {
      _call (wrapped, prop, args) {
        if (gySVG._debug === gySVG.DEBUG_NONE) {
          return;
        }
        const tag = wrapped.el.tagName;
        if (!isValidMethod (gySVGObject, tag, prop)) {
          return console.error (`${ NAME } unknown method ${ tag }.${ prop }() ${ origin () }`);
        }
        if (prop === 'add' && !isValidElement (tag, args[ 0 ])) {
          return console.error (`${ NAME } invalid element ${ tag }.${ prop }( '${ args[ 0 ] }' ) ${ origin () }`);
        }
        if (gySVG._debug >= gySVG.DEBUG_WARNING && !isValidMethodParameters (tag, prop)) {
          console.warn (`${ NAME } call ${ tag }.${ prop }( ${ toString (args) } )`);
        }
        if (gySVG._debug >= gySVG.DEBUG_ALL) {
          console.info (`${ NAME } call ${ tag }.${ prop }( ${ toString (args) } )`);
        }
      },
      _d (wrapped, prop, args) {
        if (gySVG._debug === gySVG.DEBUG_NONE) {
          return;
        }
        const tag = wrapped.el.tagName;
        if (!isValidPathD (tag, prop)) {
          return console.error (`${ NAME } unknown method ${ tag }.d.${ prop }() ${ origin () }`);
        }
        if (gySVG._debug >= gySVG.DEBUG_WARNING) {
          console.info (`${ NAME } call ${ tag }.d.${ prop }( ${ toString (args) } )`);
        }
      }
    }
  );
}

function origin () {
  try {
    throw new Error ('myError');
  } catch (e) {
    const stack = e.stack.split ('\n');
    stack.shift ();
    stack.shift ();
    stack.shift ();
    stack.shift ();
    stack.shift ();
    return stack.join ('\n').trim ();
  }
}

function toString (args) {
  return args.map (value => JSON.stringify (value, converter)).join (', ');
}

function toTypes (args) {
  return args.map (value => JSON.stringify (value, converter)).join (', ');
}

function converter (key, val) {
  if (typeof val === 'function') {
    return `[function ${ val.name }]`;
  } else if (typeof val === 'object' && Object.getPrototypeOf (val) !== Object.prototype) {
    return val.toString ();
  }
  return val;
}